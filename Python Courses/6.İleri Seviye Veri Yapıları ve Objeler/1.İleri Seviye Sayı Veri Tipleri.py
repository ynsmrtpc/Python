# abs() - mutlak değer almaya yarar
print(abs(-34))
print("-----------------------")

# round() - yuvarlama yapar 
print(round(4.3))
print(round(3.2229,3))
print(round(3.2224,3))
print("-----------------------")
# max(), min() - minimum ve maksimum değerleri bulur
print(max([1,2,4,66,3,6,7,8,4,6,8]))
print(min([1,2,4,66,3,6,7,8,4,6,8]))
print("-----------------------")
# sum() fonksiyonu - içine verilen liste veya demetin içindeki elemanları toplar
print(sum([4,3,2,4,3,4,6,5]))
print(sum((34,2,5,6,3,3,2,45,6,2)))
print("-----------------------")
# pow() fonksiyonu üs bulmakta kullanılır
print(pow(2,3))
print(pow(16,0.5))
print(pow(4,5))
print("-----------------------")